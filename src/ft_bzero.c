/* ====================================||==================================== *\
||																			  ||
||									Be Zero									  ||
||																			  ||
||		Fills the first size_t n characters of void *s with 0.				  ||
||																			  ||
\* ================libft===============||==============Â©Othello============== */

#include "libft_src.h" // mem_set

void	ft_bzero(void *s, size_t n)
{
	if (n == 0)
		return ;
	ft_memset(s, 0, n);
}
