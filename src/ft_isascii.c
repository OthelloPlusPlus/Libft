/* ====================================||==================================== *\
||																			  ||
||									Is ASCII								  ||
||																			  ||
||		Checks whether int c is an ASCII character.							  ||
||																			  ||
||		Returns 1 if true, 0 if false.										  ||
||																			  ||
\* ================libft===============||==============Â©Othello============== */

int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}
